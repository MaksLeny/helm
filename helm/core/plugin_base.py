"""
Helm — контракт плагина.

Каждый раздел дашборда (Мультимедиа, Монитор, Питание…) — это плагин:
самодостаточный модуль, который сообщает ядру про себя по единому контракту.
Ядро ничего не знает о содержимом плагина — только об этом интерфейсе.
"""
from __future__ import annotations

from typing import Awaitable, Callable, Optional

from fastapi import APIRouter

# Тип функции отправки сообщения клиенту (её даёт ядро при обработке WS).
SendFunc = Callable[[dict], Awaitable[None]]


class Plugin:
    """
    Базовый класс плагина. Наследники переопределяют то, что им нужно.

    Обязательны только id / name / icon. Остальное — по необходимости:
      - router:        HTTP-роуты плагина (если нужны)
      - frontend():    кусок HTML/CSS/JS экрана плагина
      - handle_ws():   обработка адресованных плагину WS-сообщений
      - start()/stop(): фоновые задачи (тикеры, слушатели)
    """

    #: Короткий идентификатор, по нему маршрутизируются WS-сообщения.
    id: str = ""
    #: Человекочитаемое имя для плитки в меню.
    name: str = ""
    #: SVG-иконка для плитки меню (inline, без зависимостей от шрифтов/CDN).
    #: Достаточно содержимого <svg>…</svg>. Если пусто — ядро нарисует точку.
    icon_svg: str = ""

    def __init__(self) -> None:
        #: Роутер плагина монтируется ядром под /plugin/<id>/...
        self.router: Optional[APIRouter] = None

    # --- Фронтенд -----------------------------------------------------------
    def frontend(self) -> str:
        """
        Вернуть HTML-фрагмент экрана плагина. Ядро вставит его в оболочку
        дашборда. Пустая строка = у плагина нет своего экрана.
        """
        return ""

    # --- WebSocket ----------------------------------------------------------
    async def handle_ws(self, msg: dict, send: SendFunc) -> None:
        """
        Обработать входящее WS-сообщение, адресованное этому плагину
        (ядро уже проверило поле plugin == self.id).

        send(payload) — отправить сообщение обратно этому клиенту. Ядро само
        проставит payload["plugin"] = self.id, плагину об этом думать не надо.
        """
        return None

    # --- Жизненный цикл -----------------------------------------------------
    async def start(self) -> None:
        """Поднять фоновые задачи плагина (вызывается при старте сервера)."""
        return None

    async def stop(self) -> None:
        """Остановить фоновые задачи (вызывается при остановке сервера)."""
        return None
