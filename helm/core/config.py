"""
Helm — общая конфигурация ядра.

Здесь живут только настройки уровня всей системы: сеть, сессии и хеш пароля.
Настройки конкретных разделов (например, токен Яндекса для медиаплагина)
лежат в конфиге самого плагина, а не здесь.
"""
from __future__ import annotations

import logging
import os
from pathlib import Path

# --------------------------------------------------------------------------- #
#                                  СЕТЬ                                        #
# --------------------------------------------------------------------------- #

HOST: str = "0.0.0.0"
_DEFAULT_PORT: int = 8765

# Каталог со статикой ядра (страница входа, оболочка дашборда)
WEB_DIR: Path = Path(__file__).parent.parent / "web"

# --------------------------------------------------------------------------- #
#                                СЕССИИ                                        #
# --------------------------------------------------------------------------- #

# Сколько живёт сессия после входа (секунды). По умолчанию — неделя, чтобы
# с телефона не приходилось логиниться каждый раз.
_DEFAULT_SESSION_TTL: float = 7 * 24 * 60 * 60

# Имя cookie с токеном сессии
SESSION_COOKIE: str = "helm_session"

# --------------------------------------------------------------------------- #
#                                ПАРОЛЬ                                        #
# --------------------------------------------------------------------------- #
# Пароль НЕ хранится в открытом виде — только его scrypt-хеш в формате
# "salt_hex$hash_hex". Чтобы задать свой пароль, запусти set_password.py
# и вставь полученную строку сюда (или переопредели переменной окружения
# HELM_PASSWORD_HASH).
#
# Значение по умолчанию ниже — хеш от пароля "helm" (для первого входа).
# ОБЯЗАТЕЛЬНО смени его на свой перед реальным использованием.
_DEFAULT_PASSWORD_HASH: str = (
    "0f9a1c3e5b7d92f4a6c8e0b2d4f60189"
    "$"
    "d63e3832085f61c5b9cba463c3ac872078f822964c7b58b8d4556e21da01516f"
)

PASSWORD_HASH: str = os.environ.get("HELM_PASSWORD_HASH", _DEFAULT_PASSWORD_HASH)

# --------------------------------------------------------------------------- #
#                    ПРИМЕНЕНИЕ ПОЛЬЗОВАТЕЛЬСКИХ НАСТРОЕК                       #
# --------------------------------------------------------------------------- #
# Поверх дефолтов накладываем helm_settings.json (порт/TTL/пароль), если есть.
# env-переменные остаются главными (это делает сам settings.get).
from core import settings as _settings  # noqa: E402

PORT: int = _settings.get_port(_DEFAULT_PORT)
SESSION_TTL: float = _settings.get_session_ttl(_DEFAULT_SESSION_TTL)
PASSWORD_HASH = _settings.get_password_hash(PASSWORD_HASH)

# --------------------------------------------------------------------------- #
#                              ЛОГИРОВАНИЕ                                     #
# --------------------------------------------------------------------------- #

logging.basicConfig(
    level=logging.INFO,
    format="%(asctime)s [%(levelname)s] %(name)s: %(message)s",
)
log = logging.getLogger("helm")

logging.getLogger("httpx").setLevel(logging.WARNING)
logging.getLogger("httpcore").setLevel(logging.WARNING)
